// Array com alguns valores
const array = ['C', 'A', 'B', 4, 5];

// Atribuição via desestruturação
const [a, b, c, quarto, quinto] = array;

// Agora você pode usar as variáveis atribuídas
console.log(a);  // Saída: 1
console.log(b);   // Saída: 2
console.log(c);  // Saída: 3
console.log(quarto);    // Saída: 4
console.log(quinto);    // Saída: 5
