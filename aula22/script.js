//Operadores Lógicos

// Declaração de variáveis
let x = 5;
let y = 10;
let z = 15;

// Operador E lógico (&&)
console.log("Operador E lógico (&&):", (x < y) && (y < z)); // true

// Operador OU lógico (||)
console.log("Operador OU lógico (||):", (x > y) || (y < z)); // true

// Operador NÃO lógico (!)
console.log("Operador NÃO lógico (!):", !(x == y)); // true
console.log("Operador NÃO lógico (!):", !(x < y)); // false
